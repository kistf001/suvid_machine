void afd() {
  for (int i = 0; i < 1024; i++) {
    float Vo;
    float R1 = 10000;
    float logR2, R2, T;
    float c1 = 1.211569011e-03, c2 = 2.205477891e-04, c3 = 1.422307176e-07;
    Vo = i;
    R2 = R1 * ((1024.0 / (float)Vo) - 1.0);
    logR2 = log(R2);
    T = (1.0 / (c1 + c2 * logR2 + c3 * logR2 * logR2 * logR2));
    T = T - 273.15;
    T = ((T * 9.0) / 5.0 + 32.0);
    //Serial.print(i,6);
    //Serial.print(",");
    Serial.print((int)(T * 32)), Serial.print(",");
    Serial.print("/"), Serial.print("/"), Serial.print(i), Serial.print(",");
    Serial.print(T), Serial.println();
  }
}
